function convertHalfKana(param_){
txt = "ｱｲｳｴｵｶｷｸｹｺｻｼｽｾｿﾀﾁﾂﾃﾄﾅﾆﾇﾈﾉﾊﾋﾌﾍﾎﾏﾐﾑﾒﾓﾔﾕﾖﾗﾘﾙﾚﾛﾜｦﾝｧｨｩｪｫｬｭｮｯ､｡ｰ｢｣ﾞﾟ";
zen = "アイウエオカキクケコサシスセソタチツテトナニヌネノハヒフヘホマミムメモヤユヨラリルレロワヲンァィゥェォャュョッ、。ー「」";
zen+= "　　　　　ガギグゲゴザジズゼゾダヂヅデド　　　　　バビブベボ　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　";
zen+= "　　　　　　　　　　　　　　　　　　　　　　　　　パピプペポ　　　　　　　　　　　　　　　　　　　　　　　　　　　　　";
str = "";
for (i=0; i<param_.length; i++){
c = param_.charAt(i);
cnext = param_.charAt(i+1);
n = txt.indexOf(c,0);
nnext = txt.indexOf(cnext,0);
if (n >= 0){
if (nnext == 60){
c = zen.charAt(n+60);
i++;
}else if (nnext == 61){
c = zen.charAt(n+120);
i++;
}else{
c = zen.charAt(n);
}
}
if ((n != 60) && (n != 61)){
str += c;
}
}
return str;
}

